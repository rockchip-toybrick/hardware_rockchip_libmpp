RK_S32 vp9d_merge_prob(RK_U8 *prob, RK_U32 cout0, RK_U32 cout1,
                       RK_S32 max_count, RK_S32 factor)
{
    RK_U32 cout = cout0 + cout1;

    if (cout) {
        RK_U32 prob2, prob1;

        prob1 = *prob;
        prob2 = ((cout0 << 8) + (cout >> 1)) / cout;
        prob2 = mpp_clip(prob2, 1, 255);

        cout = MPP_MIN(cout, (RK_U32)max_count);
        factor = FASTDIV(factor * cout, max_count);

        // weighted prob: function (v1 * (256 - w) + v2 * w + 128) >> 8
        *prob = prob1 + (((prob2 - prob1) * factor + 128) >> 8);
    }

    return 0;
}
