RK_S32 vp9d_diff_update_prob(Vp9dReader *r, RK_S32 prob, RK_U8 *delta)
{
    RK_S32 delp;

    if (!vp9d_read_bit(r)) {
        delp = vp9d_read_bits(r, 4) + 0;
    } else if (!vp9d_read_bit(r)) {
        delp = vp9d_read_bits(r, 4) + 16;
    } else if (!vp9d_read_bit(r)) {
        delp = vp9d_read_bits(r, 5) + 32;
    } else {
        delp = vp9d_read_bits(r, 7);
        if (delp < 65) {
            delp += 64;
        } else {
            delp = (delp << 1) - 1 + vp9d_read_bit(r);
        }
    }
    *delta = delp;
    if (prob <= 128) {
        delp = inv_recenter_nonneg(inv_map_table[delp], prob - 1);
    } else {
        delp = 255 - inv_recenter_nonneg(inv_map_table[delp], 255 - prob);
    }
    return delp;
}

